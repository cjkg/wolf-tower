def map_outline2(room_axis_length):
    while True:
        max_row_col=room_axis_length-1
        map_outline=np.zeros((room_axis_length,room_axis_length),int)
        column=np.random.randint(room_axis_length)
        row=0
        room_type=1
        room_above=0
        while True:
            x_direction=0
            y_direction=0

            room_type_number=np.random.randint(1,6)

            if room_type_number in set([1,2]):
                x_direction=-1
                y_direction=0
            elif room_type_number in set([3,4]):
                x_direction=1
                y_direction=0
            else:
                y_direction=1
            
            if row>0:
                room_above=map_outline[row-1][column]
            
            if column==0:
                x_direction=1
            elif column==max_row_col:
                x_direction=-1


            if y_direction==0 and room_above==2:
                room_type=3
            elif y_direction>0 and row<max_row_col:
                room_type=2
            elif y_direction>0 and row>=max_row_col:
                room_type=4
            elif column==0 and x_direction==-1 and row<max_row_col:
                room_type=2
                y_direction=1
            elif column==max_row_col and x_direction==1 and row<max_row_col:
                room_type=2
                y_direction=1
            elif column==max_row_col and row==max_row_col:
                room_type=4
            elif row==max_row_col and y_direction==1:
                room_type=4
            else:
                room_type=1
                y_direction=0
            
            map_outline[row][column]=room_type

            if room_type==4:
                break

            column+=x_direction
            row+=y_direction
        if np.count_nonzero(map_outline)>=.75*room_axis_length**2:
            break
    return map_outline

